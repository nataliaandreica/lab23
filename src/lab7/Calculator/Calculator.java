package lab7.Calculator;

public class Calculator {


    public int sum(int f, int s){
        return f + s;
    }

    public int substract(int f, int s){
        return f - s;
    }

    public int average(int f, int s){
        return (f + s)/2;
    }
    public int multiple(int f, int s){
        return f *s ;}
    public int division(int f, int s){
        return f /s ;}
}
